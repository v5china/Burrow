def esc(s):
    return (s.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;"))


def md_links(s):
    """Escape HTML, then convert `code`, **bold**, and [text](url) markup."""
    out = esc(s)
    out = re.sub(r"`([^`]+)`", r"<code>\1</code>", out)
    out = re.sub(r"\*\*([^*]+)\*\*", r"<b>\1</b>", out)
    return re.sub(r"\[([^\]]+)\]\(([^)]+)\)",
                  r'<a href="\2" target="_blank" rel="noopener">\1</a>', out)


def month_year(iso):
    y, m, _ = (int(p) for p in iso.split("-"))
    return f"{MONTHS[m - 1]} {y}"


def pretty_date(iso):
    y, m, d = (int(p) for p in iso.split("-"))
    return f"{MONTHS[m - 1][:3]} {d}, {y}"


def icon_svg(name):
    return ('<svg viewBox="0 0 24 24" stroke-linecap="round" '
            f'stroke-linejoin="round">{ICONS[name]}</svg>')


