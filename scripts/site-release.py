def validate_roadmap(data):
    date.fromisoformat(data["updated"])
    for col in data.get("columns", []):
        if not col.get("title"):
            sys.exit("roadmap.json: a column needs a 'title'")
        for it in col.get("items", []):
            if not isinstance(it.get("title"), str) or not it["title"].strip():
                sys.exit(f"roadmap.json: an item in '{col['title']}' needs a 'title'")
            if it.get("type") and it["type"] not in RTYPES:
                sys.exit(f"roadmap.json: unknown type '{it['type']}' in '{col['title']}' "
                         f"(valid: {', '.join(sorted(RTYPES))})")


def render_roadmap_item(it):
    note = f'\n          <p class="note">{md_links(it["note"])}</p>' if it.get("note") else ""
    tags = []
    if it.get("type"):
        tags.append(f'<span class="rtype {esc(it["type"])}">{esc(it["type"])}</span>')
    if it.get("issue"):
        n = int(it["issue"])
        tags.append(f'<a class="rissue" href="{REPO}/issues/{n}" target="_blank" rel="noopener">#{n} ↗</a>')
    if it.get("version"):
        v = esc(it["version"])
        tags.append(f'<a class="rver" href="releases.html#v{v}">{v}</a>')
    tags_html = f'\n          <div class="rtags">{"".join(tags)}</div>' if tags else ""
    return (f'        <div class="ritem">\n'
            f'          <span class="t">{md_links(it["title"])}</span>{note}{tags_html}\n'
            f'        </div>')


def render_roadmap(data):
    cols = []
    for col in data["columns"]:
        items = col.get("items", [])
        if not items:
            continue
        cards = "\n".join(render_roadmap_item(it) for it in items)
        blurb = f'<span class="blurb">{esc(col["blurb"])}</span>' if col.get("blurb") else ""
        cols.append(
            f'    <section class="rcol">\n'
            f'      <div class="rcol-head"><h2>{esc(col["title"])}</h2>'
            f'<span class="ct">{len(items)}</span>{blurb}</div>\n'
            f'      <div class="ritems">\n{cards}\n      </div>\n'
            f'    </section>')
    cta = (f'A guide, not a promise — order shifts with what people ask for. '
           f'Upvote an issue to push it up, or '
           f'<a href="{REPO}/issues/new/choose" target="_blank" rel="noopener">open a request ↗</a>. '
           f'Updated {pretty_date(data["updated"])}.')
    return page_html(
        title="Burrow · Roadmap",
        desc="What's shipped, building, and being weighed for Burrow — and how to vote.",
        og_url=f"{SITE}/roadmap.html",
        hero_h1="Roadmap",
        hero_p="What's shipped, what's being built, and what we're weighing next.",
        hero_extra=cta,
        main_html="\n".join(cols),
        extra_css=ROADMAP_CSS,
        active="roadmap",
    )


def render_readme_roadmap(data):
    """The README "## Roadmap" block — GitHub markdown, raw (not HTML)."""
    out = [RM_BEGIN,
           f"The full board, with status and voting, lives at "
           f"**[{SITE_HOST}/roadmap]({SITE}/roadmap.html)**. Vote by upvoting an "
           f"issue, or [open a request]({REPO}/issues/new/choose).", ""]
    for col in data["columns"]:
        items = col.get("items", [])
        if not items or col.get("key") == "shipped":
            continue
        out.append(f"**{col['title']}**")
        out.append("")
        for it in items:
            ref = f" ([#{int(it['issue'])}]({REPO}/issues/{int(it['issue'])}))" if it.get("issue") else ""
            note = f" — {it['note']}" if it.get("note") else ""
            out.append(f"- {it['title']}{note}{ref}")
        out.append("")
    shipped = next((c for c in data["columns"] if c.get("key") == "shipped"), None)
    if shipped and shipped.get("items"):
        names = ", ".join(i["title"] for i in shipped["items"][:5])
        out.append(f"_Recently shipped: {names} — see the [changelog]({SITE}/releases.html)._")
    out.append(RM_END)
    return "\n".join(out)


# --- patchers + main ------------------------------------------------------

def patch_index(html, rel):
    begin = html.find(WN_BEGIN)
    end = html.find(WN_END)
    if begin == -1 or end == -1:
        sys.exit("index.html: WN:BEGIN / WN:END markers not found")
    html = html[:begin] + render_whatsnew(rel) + html[end + len(WN_END):]
    # Hero version badge.
    html = re.sub(r"(github\.com/caezium/Burrow</b> · )v[0-9][^<]*",
                  rf"\g<1>v{rel['version']}", html)
    return html


def patch_readme(text, data):
    begin = text.find(RM_BEGIN)
    end = text.find(RM_END)
    if begin == -1 or end == -1:
        sys.exit("README.md: ROADMAP:BEGIN / ROADMAP:END markers not found")
    return text[:begin] + render_readme_roadmap(data) + text[end + len(RM_END):]


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--check", action="store_true",
                    help="verify generated files are up to date; exit 1 if not")
    ap.add_argument("--list-icons", action="store_true")
    args = ap.parse_args()

    if args.list_icons:
        print("icons: ", ", ".join(sorted(ICONS)))
        print("colors:", ", ".join(sorted(COLORS)))
        return

    releases = json.loads(DATA.read_text(encoding="utf-8"))["releases"]
    if not releases:
        sys.exit("releases.json: no releases")
    for r in releases:
        validate(r)
    versions = [r["version"] for r in releases]
    if sorted(versions, key=lambda v: [int(p) for p in v.split(".")], reverse=True) != versions:
        sys.exit("releases.json: releases must be ordered newest first")

    roadmap = json.loads(ROADMAP.read_text(encoding="utf-8"))
    validate_roadmap(roadmap)

    targets = {
        INDEX: patch_index(INDEX.read_text(encoding="utf-8"), releases[0]),
        HISTORY: render_history(releases),
        ROADMAP_HTML: render_roadmap(roadmap),
        README: patch_readme(README.read_text(encoding="utf-8"), roadmap),
    }

    stale = [str(p.relative_to(ROOT)) for p, new in targets.items()
             if not p.exists() or p.read_text(encoding="utf-8") != new]

    if args.check:
        if stale:
            sys.exit(f"out of sync with docs JSON: {', '.join(stale)} "
                     f"— run python3 scripts/site-release.py and commit the result")
        print("site content is in sync")
        return

    for p, new in targets.items():
        p.write_text(new, encoding="utf-8")
    print(f"regenerated: {', '.join(stale) if stale else 'nothing changed'}")


if __name__ == "__main__":
    main()
